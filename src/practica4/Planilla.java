    private void agregarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBotonActionPerformed
        num++;
        try {
            if (num > personal.length) {
                throw new RangoException("Arreglo lleno");
            }else{
                if(empleadoBoton.isSelected()){
                    personal[num] = new Empleado(
                            Integer.parseInt(codigoTexto.getText()), 
                            nombreTexto.getText(), 
                            Integer.parseInt(basicoTexto.getText()),
                            Integer.parseInt(diasFaltaTexto.getText()),
                            Integer.parseInt(horasExtraTexto.getText())
                    );
                }else{
                    personal[num] = new Consultor(Integer.parseInt(codigoTexto.getText()), 
                            nombreTexto.getText(), 
                            Integer.parseInt(basicoTexto.getText()),
                            Integer.parseInt(bonificacionTexto.getText())
                    );
                }
                personal[num].calcularRetenciones(0.21);
                personal[num].calcularNeto(num);
            }
        } catch (RangoException rangoException) {
            rangoException.mostrar();
        }
        
    }//GEN-LAST:event_agregarBotonActionPerformed

    private void buscarBotónActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBotónActionPerformed
        var codigo = Integer.parseInt(codigoTexto.getText());
        var i = 1;
        var encontrado = false;
        String mensaje;
        while(i<personal.length && !encontrado){
            if(codigo != personal[i].getCodigo()){
                i++;
            }else{
                encontrado = true;
            }
        }
        if(encontrado){
            mensaje = "Codigo: " + personal[i].getCodigo() + 
                    "\nNombre: " + personal[i].getNombre() + 
                    "\nBasico: " + personal[i].getBasico() + 
                    "\nRetenciones: " + personal[i].getRetenciones() + 
                    "\nNeto: " + personal[i].getNeto() + "\n\n";
        }else{
            mensaje = "No se encuentra";
        }
        System.out.println(mensaje);
    }//GEN-LAST:event_buscarBotónActionPerformed

