    private void mostrarBotónActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarBotónActionPerformed
        String cadena = "";
        for(int i=0; i<personal.length; i++){
            if(personal[i] != null){
                cadena = cadena + 
                        "Codigo: " + personal[i].getCodigo() + 
                        "\nNombre: " + personal[i].getNombre() + 
                        "\nBasico: " + personal[i].getBasico() + 
                        "\nRetenciones: " + personal[i].getRetenciones();
                if(personal[i] instanceof Empleado){
                    cadena = cadena +  "\nDias: " + ((Empleado) personal[i]).getDiasFaltas();
                }else{
                    cadena = cadena + "\nBonificacion: " +  ((Consultor) personal[i]).getBonificacion();
                }
                cadena = cadena + "\nNeto: " + personal[i].getNeto() + "\n\n";
            }
        }
        System.out.println(cadena);
    }//GEN-LAST:event_mostrarBotónActionPerformed

